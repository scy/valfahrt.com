<h2>Auch in diesem Jahr zieht es die Gemeinde wieder ins Tal der Heiligen Marie.</h2>
<p>
Die Sause beginnt am 8. Oktober und endet leider schon eine Woche später am 15. Oktober.
</p>
<p>
Um das leibliche und geistige Wohl Aller zu sichern, werden wir am Anreisetag in der Kapelle bei Messwein und Hostien feierlich die »Bulle zu Val«, in der geregelt wird, welcher Valfahrer sein Geschick und Können in verschiedensten Diziplinen des täglichen Zusammenlebens unter Beweis stellen sollte, erlassen.
</p>
<p>
Wir freuen uns über jeden Valfahrer, der den Weg in unsere Mitte findet.
Um Rückmeldung, sprich An- oder Abmeldung, wird bis zum 10. September gebeten.
Das kann online im <a href="anmeldung.php">Anmeldeformular</a> geschehen oder telefonisch unter <span class="tel">0621 / 12 850 577</span>.
</p>
